    public void show_Name_Movie() throws SQLException{
        String query = "select TITILE FROM movie";
        ResultSet rs = DAO.resultSet(query);
        while(rs.next()){
            jComboBox1.addItem(rs.getString(1));
        }
    }
    public void add_show_movie() throws SQLException{
        String movie_id = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        String query1 = "select MOVIE_ID FROM movie WHERE TITILE = '" + movie_id + "'";
        String date_show = jTextField1.getText();
        String start_time = jTextField2.getText();
        String fare = jTextField3.getText();
        
        ResultSet rs = DAO.resultSet(query1);
        int id_movie = 0;
        while(rs.next()){
            id_movie = rs.getInt(1);
        }
       
        String query = "INSERT INTO `show_movie`(`DATE_SHOW`, `START_TIME`, `FARE`, `MOVIE_ID`) VALUES ('"+date_show+"','"+start_time+"','"+fare+"','"+id_movie+"')";
        DAO.insertRecord(query);

        String query2 = "SELECT SHOW_ID FROM show_movie WHERE DATE_SHOW = '"+date_show+"' and START_TIME = '"+start_time+"' AND MOVIE_ID = '"+id_movie+"'";
        rs = DAO.resultSet(query2);
        int show_id = 0;
        while(rs.next()){
            show_id = rs.getInt(1);
        }
        for(int i = 1; i <= 20; ++i){
            String query_add_seat = "INSERT INTO `seat`(`SHOW_ID`, `NUM_SEAT`, `STATUS`) VALUES ('"+show_id+"','"+ i +"','0')";
            DAO.insertRecord(query_add_seat);
        }
    }
    
