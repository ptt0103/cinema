    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String movie_name = jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        String query = "select MOVIE_ID FROM movie where TITILE = '" + movie_name + "'";
        ArrayList<String> list_date = new ArrayList<>();
        int id_movie = 0;
        try {
            ResultSet rs = DAO.resultSet(query);
            while(rs.next()){
                id_movie = rs.getInt(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Fix_Movie.class.getName()).log(Level.SEVERE, null, ex);
        }
        String query1 = "select DATE_SHOW FROM show_movie where MOVIE_ID = " + id_movie;
        jComboBox2.removeAllItems();
        try {
            ResultSet rs1 = DAO.resultSet(query1);
            while(rs1.next()){
                if(!list_date.contains(rs1.getString(1))){
                    jComboBox2.addItem(rs1.getString(1));
                    list_date.add(rs1.getString(1));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Fix_Movie.class.getName()).log(Level.SEVERE, null, ex);
        }
        l_id.setMovie_id(id_movie);
        System.out.println(movie_name);
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

